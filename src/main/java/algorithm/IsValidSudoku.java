    public static boolean isValidSudoku(char[][] board) {
        char[][] row = new char[9][9];
        char[][] column = new char[9][9];
        char[][] table = new char[9][9];

        for (int i = 0; i < 9; i++) {
            for (int j = 0; j < 9; j++) {
                if (board[i][j] != '.') {
                    int k = board[i][j] - '1';
                    int x = (i / 3) * 3 + j / 3;
                    if (row[i][k] > 0 || column[j][k] > 0 || table[x][k] > 0) {
                        return false;
                    }
                    row[i][k]++;
                    column[j][k]++;
                    table[x][k]++;
                }
            }
        }
        return true;
    }

